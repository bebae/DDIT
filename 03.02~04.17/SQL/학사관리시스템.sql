SELECT A.STD_NO, A.STD_NAME, B.SUB_NAME, C.CLS_SCORE,
    CASE 
        WHEN C.CLS_SCORE >= 95 THEN 'A+' WHEN C.CLS_SCORE >=90 THEN 'A0'
        WHEN C.CLS_SCORE >= 85 THEN 'B+' WHEN C.CLS_SCORE >= 80 THEN 'B0'
        WHEN C.CLS_SCORE >= 75 THEN 'C+' WHEN C.CLS_SCORE >= 70 THEN 'C0'
        WHEN C.CLS_SCORE >= 65 THEN 'D+' WHEN C.CLS_SCORE >= 60 THEN 'D0' ELSE 'F' END 학점  
    FROM STUDENT A, SUBJECT B, CLASS C 
    WHERE SUBSTR(C.SUB_CODE, 1, 3) >= '231' AND A.STD_NO = C.STD_NO AND B.SUB_CODE = C.SUB_CODE
    ORDER BY CLS_SCORE DESC;


